int	ft_atoi(const char *c)
{
	int	sign;
	int	res;

	res = 0;
	sign = 1;
	while (*c == ' ' || *c == '\f' || *c == '\n' || *c == '\r'
		|| *c == '\t' || *c == '\v')
		c++;
	if (*c == '-' || *c == '+')
	{
		if (*c == '-')
			sign = -1;
		c++;
	}
	while (*c >= '0' && *c <= '9')
	{
		res = res * 10 + (*c - '0');
		c++;
	}
	return (sign * res);
}
